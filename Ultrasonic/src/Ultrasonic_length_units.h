#ifndef ULTRASONIC_LENGTH_UNITS_H
#define ULTRASONIC_LENGTH_UNITS_H

// General unit of length (SI unit)
#define m "m"            // Meters

// Units of length greater than meter in order of decreasing power of 10 (prefixes)
#define Em "Em"   // Exameters
#define Pm "Pm"   // Petameters
#define Tm "Tm"   // Terameters
#define Gm "Gm"   // Gigameters
#define Mm "Mm"   // Megameters
#define km "km"   // Kilometers
#define hm "hm"   // Hectometers
#define dam "dam" // Decameters

// Units of length less than meter in order of decreasing power of 10 (prefixes)
#define dm "dm"   // Decimeters
#define cm "cm"   // Centimeters
#define mm "mm"   // Milimeters
#define um "um"   // Micrometers
#define nm "nm"   // Nanometers
#define pm "pm"   // Picometers
#define fm "fm"   // Femtometers
#define am "am"   // Attometers

// Other units of length
#define in "in"           // Inches
#define ft "ft"           // Feet
#define mi "mi"           // Miles

#endif
